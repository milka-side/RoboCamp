// =========================================================
// СИСТЕМА АНІМАЦІЙ
// =========================================================
enum AnimMode {
  ANIM_NONE, ANIM_ROTATE, ANIM_BLINK, ANIM_SMILE, ANIM_CRY, ANIM_WHEEL_LEFT, ANIM_WHEEL_RIGHT
};

AnimMode current_anim = ANIM_NONE;
int current_frame = 0;
unsigned long last_frame_time = 0;

void setAnimation(AnimMode mode) {
  if (current_anim != mode) {
    current_anim = mode;
    current_frame = 0;
    last_frame_time = millis(); 
  }
}

// =========================================================
// ОСНОВНЕ НАЛАШТУВАННЯ
// =========================================================
void Car_Peripherals_Setup() {
  // Екран
  matrix.init(EMOTION_ADDRESS, EMOTION_SDA, EMOTION_SCL);
  matrix.setBrightness(15);
  Matrix_Clear();

  // Світлодіоди (WS2812 напряму через RMT)
  WS2812_Init();
  LED_TurnOffAll();
}

// =========================================================
// ФУНКЦІЇ СВІТЛОДІОДІВ
// =========================================================
void LED_SetColor(int led_number, int r, int g, int b) {
  if (led_number < 0 || led_number >= WS2812_COUNT) return;
  led_r[led_number] = (uint8_t)r;
  led_g[led_number] = (uint8_t)g;
  led_b[led_number] = (uint8_t)b;
  WS2812_Flush();
}

void LED_SetAllColors(int r, int g, int b) {
  for (int i = 0; i < WS2812_COUNT; i++) {
    led_r[i] = (uint8_t)r;
    led_g[i] = (uint8_t)g;
    led_b[i] = (uint8_t)b;
  }
  WS2812_Flush();
}

void LED_TurnOff(int led_number) {
  LED_SetColor(led_number, 0, 0, 0);
}

void LED_TurnOffAll() {
  LED_SetAllColors(0, 0, 0);
}

