// =========================================================
// WS2812 ДРАЙВЕР НАПРЯМУ ЧЕРЕЗ RMT (без сторонніх бібліотек)
// =========================================================
#define WS2812_GPIO         32
#define WS2812_COUNT        12
#define WS2812_RMT_CHANNEL  RMT_CHANNEL_0

// Таймінги WS2812 у "тіках" RMT. clk_div=2 при 80МГц APB -> 1 тік = 25нс.
#define WS2812_T0H  16   // 0.40 мкс
#define WS2812_T0L  34   // 0.85 мкс
#define WS2812_T1H  34   // 0.85 мкс
#define WS2812_T1L  16   // 0.40 мкс
#define WS2812_RESET_US 60

static uint8_t led_r[WS2812_COUNT];
static uint8_t led_g[WS2812_COUNT];
static uint8_t led_b[WS2812_COUNT];

void WS2812_Init() {
  rmt_config_t config = RMT_DEFAULT_CONFIG_TX((gpio_num_t)WS2812_GPIO, WS2812_RMT_CHANNEL);
  config.clk_div = 2; // 1 тік = 25нс
  rmt_config(&config);
  rmt_driver_install(config.channel, 0, 0);
}

// Записує буфер led_r/g/b на стрічку
static void WS2812_Flush() {
  static rmt_item32_t items[WS2812_COUNT * 24 + 1];
  int idx = 0;

  for (int i = 0; i < WS2812_COUNT; i++) {
    // Порядок передачі WS2812 — GRB
    uint8_t colors[3] = { led_g[i], led_r[i], led_b[i] };
    for (int c = 0; c < 3; c++) {
      for (int bit = 7; bit >= 0; bit--) {
        bool one = (colors[c] >> bit) & 0x01;
        items[idx].level0    = 1;
        items[idx].duration0 = one ? WS2812_T1H : WS2812_T0H;
        items[idx].level1    = 0;
        items[idx].duration1 = one ? WS2812_T1L : WS2812_T0L;
        idx++;
      }
    }
  }

  // Reset-імпульс в кінці (низький рівень, довша пауза)
  items[idx].level0 = 0;
  items[idx].duration0 = WS2812_RESET_US * 40; // *40, бо 1 тік = 25нс -> 40 тіків/мкс
  items[idx].level1 = 0;
  items[idx].duration1 = 0;
  idx++;

  rmt_write_items(WS2812_RMT_CHANNEL, items, idx, true /* wait for finish */);
}

