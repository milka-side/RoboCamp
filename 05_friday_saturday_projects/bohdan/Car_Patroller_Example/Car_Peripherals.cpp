void Matrix_Clear() {
  setAnimation(ANIM_NONE);
  matrix.showStaticArray(img_clear, img_clear);
}

void Matrix_ShowArrowLeft() {
  setAnimation(ANIM_NONE);
  matrix.showStaticArray(img_arrow_left, img_arrow_left);
}

void Matrix_ShowArrowRight() {
  setAnimation(ANIM_NONE);
  matrix.showStaticArray(img_arrow_right, img_arrow_right);
}

void Matrix_ShowStaticEmotion(int id) {
  setAnimation(ANIM_NONE);
  int count = sizeof(static_emotion_left) / sizeof(static_emotion_left[0]);
  id = constrain(id, 0, count - 1);
  matrix.showStaticArray(static_emotion_left[id], static_emotion_right[id]);
}

