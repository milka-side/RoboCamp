// =========================================================
// ФУНКЦІЇ ЕКРАНУ - АВТОМАТИЧНЕ ОНОВЛЕННЯ 
// =========================================================
void Matrix_Update() {
  if (current_anim == ANIM_NONE) return; 

  unsigned long current_time = millis();
  
  switch (current_anim) {
    case ANIM_ROTATE:
      if (current_time - last_frame_time >= 150) {
        last_frame_time = current_time;
        matrix.showStaticArray(eyeRotate1[current_frame], eyeRotate2[current_frame]);
        current_frame = (current_frame + 1) % (sizeof(eyeRotate1) / 8);
      }
      break;

    case ANIM_BLINK:
      if (current_time - last_frame_time >= 100) {
        last_frame_time = current_time;
        if (current_frame < 10) {
          matrix.showStaticArray(eyeBlink[0], eyeBlink[0]);
        } else {
          int idx = current_frame - 10;
          matrix.showStaticArray(eyeBlink[idx], eyeBlink[idx]);
        }
        current_frame++;
        if (current_frame >= 10 + (sizeof(eyeBlink) / 8)) current_frame = 0;
      }
      break;

    case ANIM_SMILE:
      if (current_time - last_frame_time >= 200) {
        last_frame_time = current_time;
        matrix.showStaticArray(eyeSmile[current_frame], eyeSmile[current_frame]);
        current_frame = (current_frame + 1) % (sizeof(eyeSmile) / 8);
      }
      break;

    case ANIM_CRY:
      if (current_time - last_frame_time >= 200) {
        last_frame_time = current_time;
        matrix.showStaticArray(eyeCry1[current_frame], eyeCry2[current_frame]);
        current_frame = (current_frame + 1) % (sizeof(eyeCry1) / 8);
      }
      break;

    case ANIM_WHEEL_LEFT:
      if (current_time - last_frame_time >= 100) {
        last_frame_time = current_time;
        matrix.showStaticArray(wheel_left[current_frame], wheel_left[current_frame]);
        current_frame = (current_frame + 1) % (sizeof(wheel_left) / 8);
      }
      break;

    case ANIM_WHEEL_RIGHT:
      if (current_time - last_frame_time >= 100) {
        last_frame_time = current_time;
        matrix.showStaticArray(wheel_right[current_frame], wheel_right[current_frame]);
        current_frame = (current_frame + 1) % (sizeof(wheel_right) / 8);
      }
      break;
      
    default:
      break;
  }
}